#include <iostream>
using namespace std;
int main()
{
	int num1,num2,sum;//initialising 3 variables
	cout<<"Enter the 1st number : ";//asking the user to enter the first number
	cin>>num1;//storing it in num1
	cout<<"Enter the 2nd number : ";//asking the user to enter second number
	cin>>num2;//storing it in num2
	sum=num1+num2;
	cout<<"The sum of "<<num1<<" and "<<num2<<" is : "<<sum;//printing the sum of the values
	return 0;
}
